JOIN pacijent
ON zakazivanje.pacijent_id=pacijent.pacijent_id
WHERE datum_zakazivanja BETWEEN '2020-03-01' AND '2020-06-30';
 
--3: Ljekari i njihova specijalnost, koji su vršili preglede u Bijeljini i Banjaluci
SELECT DISTINCT lekar.ime || ' ' || lekar.prezime AS lekar, specijalnost, grad  FROM poliklinika_specijalista
JOIN lekar ON poliklinika_specijalista.specijalista_id = lekar.lekar_id 
JOIN lekar_specijalnost ON lekar.lekar_id=lekar_specijalnost.lekar_id
JOIN specijalnost ON lekar_specijalnost.specijalnost_id = specijalnost.specijalnost_id
JOIN poliklinika ON poliklinika_specijalista.poliklinika_id = poliklinika.poliklinika_id
JOIN grad ON poliklinika.grad_id = grad.grad_id
WHERE grad IN ('Bijeljina', 'Banjaluka');

--4: Klinike čiji broj telefona počinje sa "+387", broj specijalista na njima
SELECT poliklinika.naziv, poliklinika.telefon, COUNT(lekar.ime) AS broj_specijalista FROM poliklinika_specijalista 
JOIN poliklinika ON poliklinika_specijalista.poliklinika_id = poliklinika.poliklinika_id
JOIN lekar ON poliklinika_specijalista.specijalista_id = lekar.lekar_id
WHERE poliklinika.telefon LIKE '+387%'
GROUP BY poliklinika.naziv, poliklinika.telefon;

--5: Naziv klinike, polje Regija u koje upisujemo "RS", "FBiH", "Srbija" i "Ostalo" u zavisnosti u kom se gradu nalazi klinika
SELECT naziv, grad,
CASE
WHEN (grad.grad_id <=3) THEN 'RS'
WHEN (grad.grad_id IN(4,5)) THEN 'FBiH'
WHEN (grad.grad_id IN(6,7)) THEN 'Srbija'
